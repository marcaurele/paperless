package space.paperless.controller.resources;

import org.springframework.hateoas.ResourceSupport;

public class RepositoryIdResource extends ResourceSupport {

	private String name;

	public RepositoryIdResource() {
		super();
	}

	public RepositoryIdResource(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		RepositoryIdResource other = (RepositoryIdResource) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
}
